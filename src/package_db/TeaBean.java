package package_db;

public class TeaBean {
	private String tea_name;
	private String tea_type;
	private String tea_taste;
	private String tea_effect;
	private String tea_image;
	
	public String getTea_name() {
		return tea_name;
	}
	public String getTea_type() {
		return tea_type;
	}
	public String getTea_taste() {
		return tea_taste;
	}
	public String getTea_effect() {
		return tea_effect;
	}
	public String getTea_image() {
		return tea_image;
	}
	
	public void setTea_name(String tea_name) {
		this.tea_name = tea_name;
	}
	public void setTea_type(String tea_type) {
		this.tea_type = tea_type;
	}
	public void setTea_taste(String tea_taste) {
		this.tea_taste = tea_taste;
	}
	public void setTea_effect(String tea_effect) {
		this.tea_effect = tea_effect;
	}
	public void setTea_image(String tea_image) {
		this.tea_image = tea_image;
	}
}
